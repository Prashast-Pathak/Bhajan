blocks = raw_text.strip().split('\n\n')
verses = []
for i, block in enumerate(blocks):
    lines = block.strip().split('\n')
    if len(lines) < 9:
        print(f"Skipping block {i+1} due to insufficient lines: {len(lines)}")
        continue
    
    hindi_lines = lines[1:5]
    roman_lines = lines[5:9]
    english_meaning = " ".join(lines[9:])
    
    verse_lines = []
    for j in range(4):
        vl = {
            "hindi": hindi_lines[j].strip(),
            "roman": roman_lines[j].strip(),
            "hindi_meaning": "",
            "english": english_meaning if j == 3 else "" # Put meaning on last line
        }
        verse_lines.append(vl)
        
    verses.append({
        "type": "stanza",
        "label_hindi": f"श्लोक {i+1}",
        "label_english": f"Stanza {i+1}",
        "lines": verse_lines
    })

with open('data/bhajans.json', 'r', encoding='utf-8') as f:
    data = json.load(f)

for b in data:
    if b['slug'] == 'shiv-tandav-stotram':
        b['verses'] = verses
        b['duration_minutes'] = 10
        b['language'] = "Sanskrit"
        b['composer'] = "Ravana"
        print(f"Updated {b['slug']} with {len(verses)} verses")

with open('data/bhajans.json', 'w', encoding='utf-8') as f:
    json.dump(data, f, ensure_ascii=False, indent=2)
